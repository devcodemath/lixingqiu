import turtle          # 导入海龟模块

turtle.pensize(2)      # 设定画笔尺寸
turtle.left(90)        # 海龟左转90度
turtle.fd(200)         # 海龟前进100
turtle.bk(100)         # 海龟倒退50
turtle.left(90)        # 海龟左转90度
turtle.fd(100)         # 海龟前进50
turtle.left(90)        # 海龟左转90
turtle.fd(100)         # 海龟前进50 
turtle.done()          # 海龟做完了
